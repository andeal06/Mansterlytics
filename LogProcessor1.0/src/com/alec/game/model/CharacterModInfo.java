package com.alec.game.model;

public class CharacterModInfo {
	private Integer hitCount;
	private Integer totalHitCount;
	private Double damageGain;
	private Integer totalDamage;
	
	public Integer getHitCount() {
		return hitCount;
	}
	public void setHitCount(Integer hitCount) {
		this.hitCount = hitCount;
	}
	public Integer getTotalHitCount() {
		return totalHitCount;
	}
	public void setTotalHitCount(Integer totalHitCount) {
		this.totalHitCount = totalHitCount;
	}
	public Double getDamageGain() {
		return damageGain;
	}
	public void setDamageGain(Double damageGain) {
		this.damageGain = damageGain;
	}
	public Integer getTotalDamage() {
		return totalDamage;
	}
	public void setTotalDamage(Integer totalDamage) {
		this.totalDamage = totalDamage;
	}
	
}
